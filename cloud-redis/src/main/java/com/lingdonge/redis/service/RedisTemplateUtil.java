    /**
     * 查询所有对象
     */
    public List<Object> findAll() {
        ValueOperations<String, Object> operations = redisTemplate.opsForValue();
        Collection<String> strs = redisTemplate.keys("*");
        return operations.multiGet(strs);
    }

    /**
     * @param key
     * @param <T>
     * @return
     */
    public <T> T getTo(final String key) {

        if (redisTemplate.hasKey(key)) {
            ValueOperations<Serializable, T> operations = redisTemplate.opsForValue();
            return operations.get(key);
        }
        return null;
    }

    /**
     * 获取到指定类型
     *
     * @param key
     * @param defaultVal
     * @param <T>
     * @return
     */
    public <T> T getTo(final String key, T defaultVal) {

        if (redisTemplate.hasKey(key)) {
            ValueOperations<Serializable, T> operations = redisTemplate.opsForValue();
            return operations.get(key);
        } else {
            return defaultVal;
        }
    }

    /**
     * 保存String值，带过期时间
     *
     * @param key
     * @param value
     * @param expireTime 多少秒后过期，指定过期的秒数，非指定时间过期
     * @return
     */
    public boolean set(String key, Object value, Long expireTime) {
        boolean result = false;
        try {
            ValueOperations<Serializable, Object> operations = redisTemplate.opsForValue();
            operations.set(key, value);
            redisTemplate.expire(key, expireTime, TimeUnit.SECONDS);
            result = true;
        } catch (Exception e) {
            e.printStackTrace();
        }
        return result;
    }

